public class AddTwoNumbers {
    public static void main(String[] args) {
        // Define two numbers statically
        double num1 = 5.0;
        double num2 = 7.0;

        // Add the two numbers
        double sum = num1 + num2;

        // Display the result
        System.out.println("The sum of " + num1 + " and " + num2 + " is " + sum);
    }
}
